    @Override
    public void setupOffsets(EntityPlayer player, float progress, float limbSwing, float limbSwingAmount, float ticks, float rotationYaw, float rotationPitch, boolean wearingHead, boolean wearingChest, boolean wearingLegs, boolean wearingFeet)
    {
        ModelOffset offsets = TFModelHelper.getOffsets(player);
        this.headbase.rotationPointX += offsets.headOffsetX;
        this.headbase.rotationPointY += offsets.headOffsetY;
        this.headbase.rotationPointZ += offsets.headOffsetZ;

        if (wearingChest && !wearingHead)
        {
            if (wearingLegs)
            {
                offsets.headOffsetY = 0;
            }
            else
            {
                offsets.headOffsetY = 1.25F;
                this.waistbase.rotationPointY += 1.25F;

                if (wearingFeet)
                {
                    this.upperlegL1.rotationPointY -= 1.25F;
                    this.upperlegR1.rotationPointY -= 1.25F;
                }
            }
        }

        if (wearingHead)
        {
            this.faceTarget(this.headbase, 1, rotationYaw, rotationPitch);

            if (!wearingChest)
            {
                this.headbase.rotationPointY += 0.5F;
            }
        }
    }

    @Override
    public void doActiveAnimations(EntityPlayer player, float progress, float limbSwing, float limbSwingAmount, float ticks, float rotationYaw, float rotationPitch, boolean wearingHead, boolean wearingChest, boolean wearingLegs, boolean wearingFeet)
    {
        this.applyDefaultHoldingAnimation(this.upperarmR1, this.upperarmL1, this.lowerarmR1, this.lowerarmL1);
        this.applyDefaultHittingAnimation(this.upperarmR1, this.upperarmL1, this.headbase, this.torsobase1, this.lowerarmR1, this.lowerarmL1);

        if (this.isRiding)
        {
            this.upperarmR1.rotateAngleX -= this.PI / 5F;
            this.upperarmL1.rotateAngleX -= this.PI / 5F;
            this.upperlegR1.rotateAngleX -= this.PI * 2F / 5F;
            this.upperlegL1.rotateAngleX -= this.PI * 2F / 5F;

            this.upperlegR1.rotateAngleY += this.PI / 10F;
            this.upperlegL1.rotateAngleY -= this.PI / 10F;
        }

        if (this.rightArmPose == ArmPose.BOW_AND_ARROW || this.leftArmPose == ArmPose.BOW_AND_ARROW)
        {
            this.upperarmR1.rotateAngleY += -0.1F + this.headbase.rotateAngleY;
            this.upperarmL1.rotateAngleY += 0.1F + this.headbase.rotateAngleY + 0.4F;
            this.upperarmR1.rotateAngleX += -(this.PI / 2F) + this.headbase.rotateAngleX;
            this.upperarmL1.rotateAngleX += -(this.PI / 2F) + this.headbase.rotateAngleX;
            this.upperarmR1.rotateAngleZ += MathHelper.cos(ticks * 0.09F) * 0.05F + 0.05F;
            this.upperarmL1.rotateAngleZ -= MathHelper.cos(ticks * 0.09F) * 0.05F + 0.05F;
            this.upperarmR1.rotateAngleX += MathHelper.sin(ticks * 0.067F) * 0.05F;
            this.upperarmL1.rotateAngleX -= MathHelper.sin(ticks * 0.067F) * 0.05F;
        }
    }

